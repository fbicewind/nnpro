package cn.nnnight.vo;

public class AvatarVo {

	private String url;
	private BigBox bigBox;
	private SelectedAvatar selectedAvatar;

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public BigBox getBigBox() {
		return bigBox;
	}

	public void setBigBox(BigBox bigBox) {
		this.bigBox = bigBox;
	}

	public SelectedAvatar getSelectedAvatar() {
		return selectedAvatar;
	}

	public void setSelectedAvatar(SelectedAvatar selectedAvatar) {
		this.selectedAvatar = selectedAvatar;
	}

}
